export function getRecommendation(role: string, score: number) {
  const rule = recommendationRules.find(
    r => r.role === role && score >= r.scoreMin && score <= r.scoreMax
  );
  if (!rule) {
    return {
      primary: offers.find(o => o.id === 'diagnostic-flash')!,
      secondary: offers.find(o => o.id === 'veille')!,
      argumentaire: 'Un diagnostic vous permettra de clarifier votre situation.',
    };
  }
  return {
    primary: offers.find(o => o.id === rule.primaryOfferId)!,
    secondary: offers.find(o => o.id === rule.secondaryOfferId)!,
    argumentaire: rule.argumentaire,
  };
}
